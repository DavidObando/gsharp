using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Text;
using Cs2Gs.CodeModel.Ast;
using Xunit;

namespace Cs2Gs.Tests.Coverage;

/// <summary>
/// The code-model surface contract: a reflection snapshot of every concrete
/// public <see cref="GNode"/> subclass (grouped by base) and every enum in
/// <c>Cs2Gs.CodeModel.Ast</c> must match its golden. Adding a node type or an
/// enum member fails here until printer coverage and a
/// <see cref="GNodeSamples"/> entry exist and the golden is regenerated.
/// </summary>
public class CodeModelSurfaceTests
{
    [Fact]
    public void CodeModelSurface_MatchesGolden()
    {
        string generated = BuildSnapshot();
        string goldenPath = Path.Combine(
            RepoRoot(), "tools", "cs2gs", "Cs2Gs.Tests", "Coverage", "code-model-surface.golden.txt");

        if (!File.Exists(goldenPath))
        {
            File.WriteAllText(goldenPath + ".actual", generated);
            Assert.Fail(
                $"missing golden at {goldenPath}. " +
                $"Wrote the generated snapshot to `{goldenPath}.actual`; review and move it into place.");
        }

        string golden = File.ReadAllText(goldenPath).Replace("\r\n", "\n");
        if (!string.Equals(generated, golden, StringComparison.Ordinal))
        {
            string actualPath = goldenPath + ".actual";
            File.WriteAllText(actualPath, generated);
            Assert.Fail(
                "Code-model surface snapshot drifted (a GNode subclass or Ast enum changed).\n" +
                "For each new node type: add printer coverage (GSharpPrinter) and a minimal\n" +
                "GNodeSamples entry so PrinterExhaustivenessTests exercises it, then update\n" +
                $"the golden. Wrote regenerated snapshot to `{actualPath}`.");
        }
    }

    /// <summary>
    /// Builds the deterministic code-model surface snapshot: concrete public
    /// <see cref="GNode"/> subclasses grouped by base class, then the
    /// <c>Cs2Gs.CodeModel.Ast</c> enums with their members. Ordinal ordering
    /// throughout.
    /// </summary>
    /// <returns>The snapshot text.</returns>
    internal static string BuildSnapshot()
    {
        var assembly = typeof(GNode).Assembly;
        var concrete = assembly.GetExportedTypes()
            .Where(t => t.IsClass && !t.IsAbstract && typeof(GNode).IsAssignableFrom(t))
            .ToList();

        var groups = new (string Header, Func<Type, bool> Match)[]
        {
            ("GExpression", t => typeof(GExpression).IsAssignableFrom(t)),
            ("GStatement", t => typeof(GStatement).IsAssignableFrom(t)),
            ("GMember", t => typeof(GMember).IsAssignableFrom(t)),
            ("GPattern", t => typeof(GPattern).IsAssignableFrom(t)),
            ("GTypeReference", t => typeof(GTypeReference).IsAssignableFrom(t)),
        };

        var sb = new StringBuilder();
        sb.Append("# Cs2Gs.CodeModel emit-AST surface (generated by CodeModelSurfaceTests).\n");
        sb.Append("# On drift: add printer coverage plus a GNodeSamples entry for each new node\n");
        sb.Append("# type, then replace this golden with the generated `.actual` file.\n");

        var remaining = new HashSet<Type>(concrete);
        foreach (var (header, match) in groups)
        {
            sb.Append('\n').Append('[').Append(header).Append("]\n");
            foreach (var type in concrete.Where(match).OrderBy(t => t.Name, StringComparer.Ordinal))
            {
                sb.Append(type.Name).Append('\n');
                remaining.Remove(type);
            }
        }

        sb.Append("\n[GNode (other)]\n");
        foreach (var type in remaining.OrderBy(t => t.Name, StringComparer.Ordinal))
        {
            sb.Append(type.Name).Append('\n');
        }

        sb.Append("\n[enums]\n");
        var enums = assembly.GetExportedTypes()
            .Where(t => t.IsEnum && t.Namespace == typeof(GNode).Namespace)
            .OrderBy(t => t.Name, StringComparer.Ordinal);
        foreach (var enumType in enums)
        {
            sb.Append(enumType.Name).Append(" = ").Append(string.Join(", ", Enum.GetNames(enumType))).Append('\n');
        }

        return sb.ToString();
    }

    private static string RepoRoot()
    {
        var dir = new DirectoryInfo(Path.GetDirectoryName(typeof(CodeModelSurfaceTests).Assembly.Location));
        while (dir != null)
        {
            if (File.Exists(Path.Combine(dir.FullName, "GSharp.sln")))
            {
                return dir.FullName;
            }

            dir = dir.Parent;
        }

        throw new InvalidOperationException("GSharp.sln not found above the test assembly.");
    }
}
